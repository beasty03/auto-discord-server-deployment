async def load_cogs(bot: commands.Bot, cog_list: list):
    """
    Loads all runtime cogs into the bot
    
    Args:
        bot (commands.Bot): The bot instance
        cog_list (list): List of cog names to load
    """
    logger.info("📥 Loading runtime cogs...")
    logger.info("-" * 60)
    
    loaded_count = 0
    failed_count = 0
    
    for cog_name in cog_list:
        try:
            # Load extension (cog)
            # Format: cogs.CogName (folder.filename)
            await bot.load_extension(f"cogs.{cog_name}")
            logger.info(f"✅ Loaded: {cog_name}")
            loaded_count += 1
            
        except commands.ExtensionNotFound:
            logger.error(f"❌ Failed: {cog_name} (not found)")
            failed_count += 1
        except commands.ExtensionAlreadyLoaded:
            logger.warning(f"⚠️  Warning: {cog_name} (already loaded)")
        except commands.NoEntryPointError:
            logger.error(f"❌ Failed: {cog_name} (no setup function)")
            failed_count += 1
        except commands.ExtensionFailed as e:
            logger.error(f"❌ Failed: {cog_name}")
            logger.error(f"   Error: {e}")
            failed_count += 1
        except Exception as e:
            logger.error(f"❌ Failed: {cog_name}")
            logger.error(f"   Unexpected error: {e}")
            traceback.print_exc()
            failed_count += 1
    
    logger.info("-" * 60)
    logger.info(f"✅ Successfully loaded: {loaded_count}")
    if failed_count > 0:
        logger.warning(f"❌ Failed to load: {failed_count}")
    logger.info("")

