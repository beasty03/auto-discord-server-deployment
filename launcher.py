def load_config():
    """
    Loads configuration from config.json
    
    This file is generated by setup.ps1 and contains server-specific settings
    
    Returns:
        dict: Configuration data with bot token and server settings
    """
    try:
        if not CONFIG_FILE.exists():
            logger.warning("⚠️  WARNING: config.json not found!")
            logger.warning(f"   Expected location: {CONFIG_FILE}")
            logger.warning("   Using values from variables.py only")
            return {}
        
        with open(CONFIG_FILE, 'r', encoding='utf-8') as f:
            config = json.load(f)
        
        logger.info("✅ Configuration loaded from config.json")
        return config
        
    except json.JSONDecodeError as e:
        logger.error(f"❌ ERROR: config.json is not valid JSON format")
        logger.error(f"   Details: {e}")
        sys.exit(1)
    except Exception as e:
        logger.error(f"❌ ERROR: Could not load configuration")
        logger.error(f"   Details: {e}")
        sys.exit(1)

