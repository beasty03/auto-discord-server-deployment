def create_bot(config: dict):
    """
    Creates and configures the Discord bot instance
    
    Args:
        config (dict): Configuration data from config.json
        
    Returns:
        commands.Bot: Configured bot instance
    """
    logger.info("🤖 Initializing bot...")
    
    # Intents determine which Discord events the bot can receive
    # Intents.all() gives access to all events (members, messages, etc.)
    intents = discord.Intents.all()
    
    # Create bot instance
    # command_prefix: The character for commands (e.g., !help)
    # intents: Which Discord events the bot may receive
    # help_command=None: Disable default help command (create your own!)
    bot = commands.Bot(
        command_prefix=COMMAND_PREFIX,
        intents=intents,
        help_command=None
    )
    
    # Add custom attributes to bot instance
    bot.config = config
    bot.start_time = datetime.utcnow()
    bot.server_name = config.get('server', {}).get('name', 'Unknown Server')
    bot.guild_id = config.get('server', {}).get('guild_id', GUILD_ID)
    
    logger.info(f"✅ Bot initialized")
    logger.info(f"   Server: {bot.server_name}")
    logger.info(f"   Prefix: {COMMAND_PREFIX}")
    logger.info(f"   Guild ID: {bot.guild_id}\n")
    
    return bot

